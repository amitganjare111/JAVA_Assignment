package Operator2;

//Write a method for increment and decrement operators(++, --)

public class IncDec {

	static void IncDecr()
	{
		int a=10;
		a++; //Increment
		System.out.println(a);
		
		int b=20;
		b--;
		System.out.println(b);
	}
	public static void main(String[] args) {

		IncDec.IncDecr();

	}

}
